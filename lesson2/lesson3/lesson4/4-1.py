names = ["Alice", "Bob", "Charlie"]
scores = [85, 92, 78]

print(names)
print(scores)

print(names[0])  # Accessing the first name
print(names[1])  # Accessing the second name
print(names[2])  # Accessing the third name

print(scores[0])  # Accessing the first score
print(scores[1])  # Accessing the second score
print(scores[2])  # Accessing the third score

print(len(names))  # Length of the names list
print(len(scores))  # Length of the scores list
print(names[len(names) - 1])  # Accessing the last name using length
print(names[-2])  # Accessing the second-to-last name using negative indexing
print(scores[-2])  # Accessing the second-to-last score using negative indexing
