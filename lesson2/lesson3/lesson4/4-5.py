# list[start:stop]

numbers = [1, 2, 3, 4, 5]
print(numbers[0:4])  # Accessing elements from index 0 to 3
print(numbers[:3])   # Accessing elements from the beginning to index 2
print(numbers[2:5])  # Accessing elements from index 2 to 4
print(numbers[::2])  # Accessing every second element
print(numbers[::-1]) # Accessing elements in reverse order
print(numbers[1:4])  # Accessing elements from index 1 to 3