numbers = [1, 2, 3, 4, 5]
print(numbers)
numbers.remove(3)  # Removing the number 3 from the list
print(numbers)

numbers.pop(1)  # Removing the element at index 1 (which is 2)
print(numbers)

numbers.pop()  # Removing the third-to-last element from the list
print(numbers)
numbers.clear()  # Clearing all elements from the list
print(numbers)

