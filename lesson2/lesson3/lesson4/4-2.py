matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]
print(matrix)
print(matrix[0])  # Accessing the first row
print(matrix[1])  # Accessing the second row
print(matrix[2])  # Accessing the third row
print(matrix[-1])  # Accessing the last row using negative indexing
print(matrix[0][1])  # Accessing the second element of the first row
print(matrix[-1][-1])  # Accessing the last element of the last row using negative indexing